public class A4_oddEven {
    public static void main(String[] args) {
        System.out.print("Even no. are : ");
        for(int i=0;i<100;i+=2){
            System.out.print(i+" ");
        }
        System.out.print("\nOdd no. are : ");
        for(int i=1;i<100;i+=2){
            System.out.print(i+" ");
        }
    }
}
